class Rectangle {
  int _width = 0;   // private
  int _length = 0;  // private

  // GETTER untuk width
  int get width {
    return _width;
  }

  // SETTER untuk width
  set width(int value) {
    _width = value;
  }

  // GETTER untuk length
  int get length {
    return _length;
  }

  // SETTER untuk length
  set length(int value) {
    _length = value;
  }
}
